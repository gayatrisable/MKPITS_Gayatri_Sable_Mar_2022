using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace print_1_to_10_do_while
{
    internal class Program
    {
        static void Main(string[] args)
        {
            for (int n = 1; n <= 10; n++)
            {
                Console.WriteLine(n);
            }
            Console.ReadLine();
        }
    }
}
